        const container = document.getElementById('container');
        const page = document.getElementById('page');
        const checkoutForm = document.getElementById('checkoutForm');
        const backbtn = document.getElementById('backbtn');
        const checkoutBtn = document.getElementById('checkoutBtn');
        const radioA = document.getElementById('radioA');
        const radioB = document.getElementById('radioB');
        const visaDetails = document.getElementById('visaDetails');
        const cashDetails = document.getElementById('cash-details');
        const topButton = document.getElementById('topButton');

        var totalSum = 0;
        var cart = {};

       
        visaDetails.style.display = 'block';
        cashDetails.style.display = 'none';

        
        data.country.forEach(country => {
            const countryName = country.name1 || country.name2 || country.name3 || country.name4 || country.name5 || country.name6;

            country.item.forEach(item => {
                const div = document.createElement('div');
                div.classList.add('item');

                const incbtn = document.createElement('button');
                incbtn.classList.add("incbtn");
                incbtn.innerHTML = '+';

                const cont = document.createElement('span');
                cont.classList.add('cont');
                cont.innerHTML = '0';

                const decbtn = document.createElement('button');
                decbtn.classList.add("decbtn");
                decbtn.innerHTML = '-';

                div.innerHTML = `
                    <img src="${item.image}" alt="${item.name}">
                    <div class="item-details">
                        <strong>${item.name}</strong>
                        <p>Price: ${item.price}</p>
                        <p>Country: ${countryName}</p>
                    </div>
                    <div class="quantity-controls">
                        ${decbtn.outerHTML}
                        ${cont.outerHTML}
                        ${incbtn.outerHTML}
                    </div>
                `;

                container.appendChild(div);

                var number = 0;

                
                const incButton = div.querySelector('.incbtn');
                const decButton = div.querySelector('.decbtn');
                const countSpan = div.querySelector('.cont');

                incButton.addEventListener('click', () => {
                    number++;
                    countSpan.innerHTML = number;
                    totalSum += Number(item.price);
                    cart[item.name] = {price: Number(item.price), quantity: number, image: item.image};
                });

                decButton.addEventListener('click', () => {
                    if (number > 0) {
                        number--;
                        countSpan.innerHTML = number;
                        totalSum -= Number(item.price);
                        if (number === 0) {
                            delete cart[item.name];
                        } else {
                            cart[item.name] = {price: Number(item.price), quantity: number, image: item.image};
                        }
                    }
                });
            });
        });

        
        document.getElementById('addToCartBtn').addEventListener('click', () => {
            backbtn.style.display = 'block';
            container.style.display = 'none';
            page.style.display = 'block';
            checkoutForm.style.display = 'none';

            const cartContainer = document.getElementById('cartContainer');
            cartContainer.innerHTML = '';

            const orderblock = document.createElement('div');
            orderblock.id = 'orderblock';

            let hasItems = false;
            for (let itemName in cart) {
                if (cart[itemName].quantity > 0) {
                    hasItems = true;
                    const order = document.createElement('div');
                    order.classList.add('orderItem');
                    const item = cart[itemName];

                    order.innerHTML = `
                        <img src="${item.image}" alt="${itemName}">
                        <div>
                            <strong>${itemName}</strong><br>
                            Price: ${item.price}<br>
                            Quantity: ${item.quantity}<br>
                            Total: ${item.price * item.quantity}<br>
                        </div>
                    `;
                    orderblock.appendChild(order);
                }
            }

            if (hasItems) {
                const totall = document.createElement('div');
                totall.classList.add('totall');
                totall.innerHTML = `Total amount: ${totalSum}`;
                orderblock.appendChild(totall);
                cartContainer.appendChild(orderblock);
                checkoutBtn.style.display = 'block';
            } else {
                cartContainer.innerHTML = '<p>Your cart is empty</p>';
                checkoutBtn.style.display = 'none';
            }
        });

        
        backbtn.addEventListener('click', () => {
            container.style.display = 'flex';
            backbtn.style.display = 'none';
            page.style.display = 'none';
            checkoutForm.style.display = 'none';
        });

        
        checkoutBtn.addEventListener('click', () => {
            page.style.display = 'none';
            checkoutForm.style.display = 'block';
        });

        
        radioA.addEventListener('change', function() {
            if (radioA.checked) {
                visaDetails.style.display = 'block';
                cashDetails.style.display = 'none';
            }
        });

        radioB.addEventListener('change', function() {
            if (radioB.checked) {
                cashDetails.style.display = 'block';
                visaDetails.style.display = 'none';
            }
        });

        
        window.addEventListener('scroll', function() {
            if (window.scrollY > 200) {
                topButton.style.display = 'block';
            } else {
                topButton.style.display = 'none';
            }
        });

        topButton.addEventListener('click', function() {
            window.scrollTo({top: 0, behavior: 'smooth'});
        });

        
        document.getElementById('paymentForm').addEventListener('submit', function(e) {
            e.preventDefault();
            alert('Order placed successfully!');
            
            cart = {};
            totalSum = 0;
            container.style.display = 'flex';
            page.style.display = 'none';
            checkoutForm.style.display = 'none';
            backbtn.style.display = 'none';
            
            
            document.querySelectorAll('.cont').forEach(el => {
                el.innerHTML = '0';
            });
        });